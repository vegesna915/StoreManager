package com.generalstore.jayambica.storemanager.Objects;


public class Vendor {

    private String vendorId;


    private String vendorName;
    private String vendorPhone;
    private String vendorEmail;
    private String vendorStreet;
    private String vendorCity;
    private String vendorState;
    private String vendorCountry;
    private String vendorZip;
    private String vendorIsUpdatedToServer;

    public String getVendorId() {
        return vendorId;
    }

    public void setVendorId(String vendorId) {
        this.vendorId = vendorId;
    }

    public String getVendorName() {
        return vendorName;
    }

    public void setVendorName(String vendorName) {
        this.vendorName = vendorName;
    }

    public String getVendorPhone() {
        return vendorPhone;
    }

    public void setVendorPhone(String vendorPhone) {
        this.vendorPhone = vendorPhone;
    }

    public String getVendorEmail() {
        return vendorEmail;
    }

    public void setVendorEmail(String vendorEmail) {
        this.vendorEmail = vendorEmail;
    }

    public String getVendorStreet() {
        return vendorStreet;
    }

    public void setVendorStreet(String vendorStreet) {
        this.vendorStreet = vendorStreet;
    }

    public String getVendorCity() {
        return vendorCity;
    }

    public void setVendorCity(String vendorCity) {
        this.vendorCity = vendorCity;
    }

    public String getVendorState() {
        return vendorState;
    }

    public void setVendorState(String vendorState) {
        this.vendorState = vendorState;
    }

    public String getVendorCountry() {
        return vendorCountry;
    }

    public void setVendorCountry(String vendorCountry) {
        this.vendorCountry = vendorCountry;
    }

    public String getVendorZip() {
        return vendorZip;
    }

    public void setVendorZip(String vendorZip) {
        this.vendorZip = vendorZip;
    }

    public String getVendorIsUpdatedToServer() {
        return vendorIsUpdatedToServer;
    }

    public void setVendorIsUpdatedToServer(String vendorIsUpdatedToServer) {
        this.vendorIsUpdatedToServer = vendorIsUpdatedToServer;
    }
}
